def handleEvents():
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            pygame.quit()
            exit()

def drawGrid(screen, screenWidth, screenHeight, tileSize):
    for line in range(0, (screenWidth // tileSize)):
        pygame.draw.line(screen, '#df3f11', (0, line * tileSize), (screenWidth, line * tileSize), 3)
        pygame.draw.line(screen, '#d00f11', (line * tileSize, 0), (line * tileSize, screenHeight), 3)

